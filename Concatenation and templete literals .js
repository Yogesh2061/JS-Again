// Concatenation 
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName +" is 22 years old.";
console.log(fullName); // Output: John Doe

//string concatenation can become cumbersome and less readable, especially with longer strings and multiple variables.

// Template Literals
let age = 22;
let message = `my name is ${firstName} and I am ${age} years old.`;
console.log(message); // Output: my name is John and I am 22 years old.

